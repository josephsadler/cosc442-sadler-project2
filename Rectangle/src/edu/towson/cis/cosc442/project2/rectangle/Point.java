/**
 * The Point Class.
 */
public class Point {
	
	/** x and y coordinates. */
	public Double x, y;
	
	/**
	 * Instantiates a new point.
	 *
	 * @param xVal the x
	 * @param yVal the y
	 */
	Point(Double xVal, Double yVal) {
		x = xVal; //x = y was corrected
		y = yVal;
	}
}
